print("Demonstration of sequence data type")

ldata = [10, 20, 30, 40]                                               # list
tdata = (10, 20, 30, 40)                                               # tuple
sdata = {10, 20, 30, 40}                                               # set
ddata = {"c": 400, "c++": 550, "java": 900, "python": 890}             # dictionary

print(ldata)
print(tdata)
print(sdata)
print(ddata)

print(type(ldata))
print(type(tdata))
print(type(sdata))
print(type(ddata))